#include "libftprintf.h"

int	ft_string(char *str)
{
	int	i;

	i = 0;
	while (str == 0)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
